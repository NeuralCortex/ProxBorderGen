package com.fx.swing.pojo;

public class InfoPOJO {
  
    private final String param;
    private final String value;

    public InfoPOJO(String param, String value) {
        this.param = param;
        this.value = value;
    }

    public String getParam() {
        return param;
    }

    public String getValue() {
        return value;
    }
}
